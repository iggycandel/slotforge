/** Rich project context from the Theme panel — fed directly into AI prompts */
export interface ProjectMeta {
  /** Game title (e.g. "Lucky Bull") */
  gameName?:       string
  /** Theme key (e.g. "western") */
  themeKey?:       string
  /** Setting / World free-text */
  setting?:        string
  /** Narrative arc free-text */
  story?:          string
  /** Mood / Tone selection */
  mood?:           string
  /** Bonus Round Narrative */
  bonusNarrative?: string
  /** Art Style selection. New projects set a styleId token here
   *  ("realistic_3d") that resolves via GRAPHIC_STYLES. Legacy projects
   *  set a free-text label ("Realistic") which promptBuilder maps through
   *  legacyStyleToId on the server. */
  artStyle?:       string
  /** Canonical style token (GRAPHIC_STYLES id). Preferred over artStyle
   *  when both are present. */
  styleId?:        string
  /** Visual Inspiration / Art Reference — free text. */
  artRef?:         string
  /** Uploaded reference images + their GPT-4o-generated STYLE descriptions.
   *  The image URLs go to Supabase Storage (CDN URLs) so the payload
   *  stays small. The description is what actually gets injected into
   *  the prompt at generation time — the image bytes aren't seen by
   *  gpt-image-1 (it's a text-to-image API). See
   *  /api/references/describe for how descriptions are produced.
   *  Capped at 3 entries by the UI; the builder iterates whatever is
   *  present, so ignore-or-include is free server-side. */
  artRefImages?:   Array<{
    id:          string
    url:         string
    description: string
  }>
  /** The project's "art bible" — a single locked-in visual signature
   *  (palette, material treatment, lighting language, ornament density,
   *  line work) that gets injected into EVERY prompt as a high-priority
   *  context line. Solves the cross-asset cohesion problem: even with
   *  the same style + theme + palette + references, separate generation
   *  calls drift because the image model doesn't know what the
   *  project's prior assets actually look like. Generated by running
   *  /api/references/describe with kind:'art_bible' over an anchor asset
   *  the user has already approved (logo / character / background_base /
   *  any uploaded asset).  Editable — once generated the user can tweak
   *  the description before it locks in. */
  artBible?: {
    /** The anchor asset key the description was derived from (e.g.
     *  'logo', 'character', 'background_base', or a custom upload key).
     *  Stored so the panel can show "generated from <type>" and offer
     *  a one-click "regenerate from same anchor" action. */
    anchorAssetKey?: string
    /** CDN URL of the anchor image at the time of generation. Kept for
     *  thumbnail rendering even if the underlying asset is later
     *  re-generated; the bible should NOT auto-update when its anchor
     *  changes (that's a manual user action). */
    anchorUrl?:      string
    /** The vision-describe output. ~120 words. This is what actually
     *  gets injected into prompts. Free-text — the user can edit. */
    description:    string
    /** ISO timestamp — when the description was generated. Shown as
     *  "locked 2 hours ago" in the panel. */
    generatedAt:    string
  }
  /** Art Direction Notes */
  artNotes?:       string
  /** Colour palette primary */
  colorPrimary?:   string
  /** Colour palette background */
  colorBg?:        string
  /** Colour palette accent */
  colorAccent?:    string
  /** Whether the matching colour is "active" — when false the prompt
   *  builder skips that tone entirely (it's not just hidden from the
   *  palette UI). Defaults to true when unset so older payloads with
   *  the three hexes but no toggle bits still behave. */
  colorPrimaryOn?: boolean
  colorBgOn?:      boolean
  colorAccentOn?:  boolean
  /** Symbol counts */
  symbolHighCount?:    number
  symbolLowCount?:     number
  symbolSpecialCount?: number
  /** Per-symbol names (from the Symbols panel) */
  symbolHighNames?:    string[]
  symbolLowNames?:     string[]
  symbolSpecialNames?: string[]
}

