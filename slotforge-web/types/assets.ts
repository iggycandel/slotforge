// ─── Generated asset record (what we store in DB) ───────────────────────────

export interface GeneratedAsset {
  id:         string
  project_id: string
  type:       AssetType
  url:        string
  prompt:     string
  theme:      string
  provider:   'runway' | 'openai' | 'mock' | 'upload'
  created_at: string
}

// ─── Generation pipeline results ────────────────────────────────────────────

export interface GenerationResult {
  backgrounds: {
    base:  GeneratedAsset
    bonus: GeneratedAsset
  }
  symbols: {
    high:    GeneratedAsset[]   // length 5
    low:     GeneratedAsset[]   // length 5
    wild:    GeneratedAsset
    scatter: GeneratedAsset
  }
  logo: GeneratedAsset
}

// ─── Aspect ratio ────────────────────────────────────────────────────────────
// User-selectable output aspect ratio. Each provider is mapped to its nearest
// supported size in lib/ai/index.ts. When the caller does not specify a ratio
// we fall back to DEFAULT_RATIO_FOR_ASSET (also in lib/ai/index.ts) — which
// encodes the natural aspect per asset type (symbols 1:1, logo 3:1, etc.).
export type AspectRatio =
  | '1:1'     // square — symbols, UI buttons, jackpot tier badges
  | '3:2'     // classic background landscape
  | '2:3'     // portrait — characters, tall overlays
  | '16:9'    // wide banner — backgrounds in widescreen
  | '9:16'    // vertical banner — mobile portrait
  | '3:1'     // title banner — logo, intro/outro banners
  | '4:1'     // extra-wide banner — HUD / footer rows
  | '1:4'     // extra-tall column — expanded reel overlay

// ─── Generation request ──────────────────────────────────────────────────────

export interface GenerateRequest {
  theme:        string      // user's raw prompt e.g. "ancient egypt"
  project_id:   string
  provider?:    'runway' | 'openai' | 'auto'
  style_id?:    string      // graphic style ID from GRAPHIC_STYLES (e.g. 'cartoon_3d')
  project_meta?: ProjectMeta
  /** When set, only generate these specific types (used for "fill gaps" mode). */
  asset_types?: AssetType[]
  /** Optional per-asset ratio override. Bulk calls apply this to every
   *  asset unless the asset type has its own default. */
  ratio?:       AspectRatio
  /** Per-slot prompt overrides (keyed on asset_type / feature slot key).
   *  When a type has an entry here, the pipeline substitutes the value
   *  for the composed prompt before calling the provider. Empty / missing
   *  entries fall through to normal composition. */
  custom_prompts?: Record<string, string>
}

export interface GenerateResponse {
  success: boolean
  result?: GenerationResult
  error?:  string
  // Per-asset errors if some succeeded and some failed
  partial?: Partial<GenerationResult>
  failed?:  Array<{ type: AssetType; error: string }>
}

